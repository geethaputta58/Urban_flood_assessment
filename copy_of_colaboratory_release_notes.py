known = df[df['elevation_m'].notna()]
missing = df[df['elevation_m'].isna()]

tree = cKDTree(known[['longitude', 'latitude']])

distances, indices = tree.query(missing[['longitude', 'latitude']], k=5)

known_elev = known['elevation_m'].values
neighbor_elev = known_elev[indices]

weights = 1 / distances
weights[distances == 0] = 1e10

idw_values = np.sum(weights * neighbor_elev, axis=1) / np.sum(weights, axis=1)

df.loc[df['elevation_m'].isna(), 'elevation_m'] = idw_values

