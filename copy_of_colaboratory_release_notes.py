# Split known and missing
known = df[df['storm_drain_proximity_m'].notna()]
missing = df[df['storm_drain_proximity_m'].isna()]

features = [
    'elevation_m',
    'drainage_density_km_per_km2',
    'latitude',
    'longitude'
]

# Train model
rf = RandomForestRegressor(n_estimators=100, random_state=42)
rf.fit(known[features], known['storm_drain_proximity_m'])

# Predict missing
predicted_values = rf.predict(missing[features])

# Fill values
df.loc[df['storm_drain_proximity_m'].isna(),
       'storm_drain_proximity_m'] = predicted_values

