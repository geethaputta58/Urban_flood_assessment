import pandas as pd
from pytorch_tabnet.tab_model import TabNetClassifier
from sklearn.metrics import accuracy_score, f1_score, hamming_loss, confusion_matrix

# =========================
# LOAD TRAIN DATA
# =========================
train_df = pd.read_csv("train_split.csv")   # or "flood_random_split_train.csv"

input_cols = ["y_low_lying", "y_extreme_rain_history"]
output_cols = ["y_monitor", "y_ponding_hotspot", "y_sparse_drainage"]

X_train = train_df[input_cols].values
Y_train = train_df[output_cols].copy()

# =========================
# TRAIN ONE MODEL PER TARGET
# =========================
train_preds = {}

for col in output_cols:
    print(f"\nTraining TabNet for: {col}")

    model = TabNetClassifier(seed=42, verbose=0)

    model.fit(
        X_train=X_train,
        y_train=Y_train[col].values,
        max_epochs=100,
        patience=10,
        batch_size=256,
        virtual_batch_size=128
    )

    # Save each target model
    model.save_model(f"tabnet_{col}")

    # Predict on training data
    pred = model.predict(X_train)
    train_preds[col] = pred

# =========================
# TRAIN PREDICTIONS DATAFRAME
# =========================
Y_train_pred_df = pd.DataFrame(train_preds, index=Y_train.index)

# =========================
# TRAIN METRICS
# =========================
