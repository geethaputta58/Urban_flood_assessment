import pandas as pd
import joblib
from lightgbm import LGBMClassifier
from sklearn.multioutput import MultiOutputClassifier
from sklearn.metrics import accuracy_score, f1_score, hamming_loss, confusion_matrix

# =========================
# LOAD TRAIN DATA
# =========================
train_df = pd.read_csv("train_split.csv")

input_cols = ["y_low_lying", "y_extreme_rain_history"]
output_cols = ["y_monitor", "y_ponding_hotspot", "y_sparse_drainage"]

X_train = train_df[input_cols]
Y_train = train_df[output_cols]

# =========================
# MODEL
# =========================
base_model = LGBMClassifier(
    n_estimators=100,
    learning_rate=0.05,
    random_state=42
)

model = MultiOutputClassifier(base_model)

# =========================
# TRAIN
# =========================
model.fit(X_train, Y_train)

# Save model
joblib.dump(model, "lightgbm_multioutput_model.pkl")

# =========================
# TRAIN PREDICTIONS
# =========================
Y_train_pred = model.predict(X_train)
Y_train_pred_df = pd.DataFrame(Y_train_pred, columns=output_cols, index=Y_train.index)

# =========================
# TRAIN METRICS
# =========================
print("\n========== LIGHTGBM TRAINING METRICS ==========\n")

exact_match_train = accuracy_score(Y_train, Y_train_pred_df)
hamming_train = hamming_loss(Y_train, Y_train_pred_df)

print("Exact Match Accuracy (Train):", exact_match_train)
print("Hamming Loss (Train):", hamming_train)

for col in output_cols:
    f1 = f1_score(Y_train[col], Y_train_pred_df[col], zero_division=0)
    cm = confusion_matrix(Y_train[col], Y_train_pred_df[col])

    print(f"\n----- {col} -----")
    print("F1-score:", f1)
    print("Confusion Matrix:\n", cm)

# =========================
# SAVE TRAIN PREDICTIONS
# =========================
train_results = pd.concat(
    [
        X_train.reset_index(drop=True),
        Y_train.reset_index(drop=True).add_prefix("actual_"),
        Y_train_pred_df.reset_index(drop=True).add_prefix("pred_")
    ],
    axis=1
)

train_results.to_csv("lightgbm_train_predictions.csv", index=False)

print("\nSaved model: lightgbm_multioutput_model.pkl")
print("Saved predictions: lightgbm_train_predictions.csv")

